public class OperadorTernario {
    public static void main(String args[]){
        // Operador Ternario
        // Ternario = 3
        // Un operador ternario se usa para establecer un comando que consta de 3 partes, una condición y dos posibles resultados
        var boolResultado = (1 > 2) ? true : false;
        print("1 > 2 ? " + boolResultado);
        
    }
    // Módulo para facilitar la impresión de cadenas
    public static void print(String cVar){
        System.out.println(cVar);
    }
}
