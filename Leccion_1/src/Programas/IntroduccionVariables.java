public class IntroduccionVariables {
    public static void main(String args[]){
        // Definimos una variable de tipo entero
        int iVar = 10;
        System.out.println(iVar);
        // Modificamos la variable de tipo entero
        iVar = iVar / 2;
        System.out.println(iVar);
        
        // Definimos una variable de tipo string
        String cVar = "Hola Mundo de Java";
        System.out.println(cVar);
        // Modificamos la variable de tipo string
        cVar = "Adios Mundo de Java";
        System.out.println(cVar);
            
    }
    
}
